    private void jButton_XOAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_XOAActionPerformed
        // TODO add your handling code here:
        int row = dsTable_SV.getSelectedRow();
        if (row != -1) {
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn Xoá không?");
            if (confirm == JOptionPane.YES_OPTION) {
                String masv = String.valueOf(dsTable_SV.getValueAt(row, 1));
                if (SinhVienController.ktTrung_MASV(masv) == null) {
                    JOptionPane.showMessageDialog(this, "Mã sinh vien không tồn tại!!!");
                }else if(DiemController.ktSinhVien(masv)==true){
                     JOptionPane.showMessageDialog(this, "Sinh vien tồn tại trong loptc không thể xoá!!!");
                } 
                else {
                    try {
                        SinhVienController.xoaSinhVien(masv);
                        layDanhSachSinhVien();
                        JOptionPane.showMessageDialog(this, "Xoá thành công!!!");
                    } catch (Exception ex) {
                        Logger.getLogger(QuanLy_SV.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            }

        } else {
            JOptionPane.showMessageDialog(this, "Chọn Mã Sinh viên cần Xoá!!! ");
        }
    }//GEN-LAST:event_jButton_XOAActionPerformed

