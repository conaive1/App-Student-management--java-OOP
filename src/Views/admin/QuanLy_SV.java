    private void jButton_THEMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_THEMActionPerformed
        // TODO add your handling code here:
        String Gioitinh = null;
        String MASV = jTextField_MASV.getText().toUpperCase().trim();
        String HoTen = jTextField_HOTEN.getText().trim();
        String NgaySinh = jTextField_NGAYSINH.getText().trim();
        String SDT = jTextField_SDT.getText().trim();
        String DiaChi = jTextField_DIACHI.getText().trim();
        String NAMNH = jTextField_NAMNH.getText().trim();
        if (ktDiachi == true && ktHoten == true && ktMssv == true && ktNamNh == true && ktSDT == true && ktdate == true) {
            if (jComboBox_MALOP.getSelectedItem().equals("--Chọn Lớp--")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn lớp!!!");
            } else if (!jRadioButton_NAM.isSelected() && !jRadioButton_NU.isSelected()) {
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn giới tính!!!");
            } else if (SinhVienController.ktTrung_MASV(MASV) != null) {
                JOptionPane.showMessageDialog(this, "Mã sinh viên không được trùng!!!");
            } else {
                HoTen = xuly_All.chuanHoaDanhTuRieng(HoTen);
                DiaChi = xuly_All.chuanHoaDanhTuRieng(DiaChi);
                if (jRadioButton_NAM.isSelected()) {
                    Gioitinh = jRadioButton_NAM.getText();
                } else {
                    Gioitinh = jRadioButton_NU.getText();
                }
                int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn Thêm không?");
                if (confirm == JOptionPane.YES_OPTION) {
                    try {
                        Sinhvien sv = new Sinhvien(MASV, HoTen, NgaySinh, DiaChi, Integer.valueOf(NAMNH), (String) jComboBox_MALOP.getSelectedItem(), SDT, Gioitinh, "123456");
                        SinhVienController.themSinhVien(sv);
                        setAlljTextField();
                        layDanhSachSinhVien();
                        ktMssv = false;
                        ktHoten = false;
                        ktDiachi = false;
                        ktNamNh = false;
                        ktdate = false;
                        ktSDT = false;
                        JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
                    } catch (Exception ex) {
                        Logger.getLogger(QuanLy_SV.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống!!!");
        }
    }//GEN-LAST:event_jButton_THEMActionPerformed

