    private void jButton_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_xoaActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chọn Mã lớp cần xoá!!!");
        } else {

            int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn Xóa không?");
            if (confirm == JOptionPane.YES_OPTION) {
                int maLop = Integer.valueOf(String.valueOf(jTable1.getValueAt(row, 0)));
                if (DiemController.ktLopTC(maLop) == true) {
                    JOptionPane.showMessageDialog(rootPane, "Lớp tín chỉ đang có sinh viên học!!!");
                } else {
                    LopTCController.xoaLopTC(maLop);
                    list.remove(row);
                    resetTable();
                    try {
                        showTable();
                    } catch (Exception ex) {
                        Logger.getLogger(QLLopTC.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton_xoaActionPerformed

