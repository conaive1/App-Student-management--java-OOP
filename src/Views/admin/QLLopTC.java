    private void jButton_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_themActionPerformed

        if (ktHK == true && ktNhom == true && ktsvmax == true && ktsvmin == true) {
            LopTC loptc = new LopTC();
            loptc.setMaLop(Integer.valueOf(jTextField_maLop.getText()));
            loptc.setMaMH((String) jComboBox_maMonHoc.getSelectedItem());
            loptc.setNienKhoa((String) jComboBox_nienKhoa.getSelectedItem());
            loptc.setHocKy(Integer.parseInt(jTextField_hocKy.getText()));
            loptc.setNhom(Integer.parseInt(jTextField_nhom.getText()));
            loptc.setSvMax(Integer.parseInt(jTextField_svmax.getText()));
            loptc.setSvMin(Integer.parseInt(jTextField_svmin.getText()));
            LopTC ktLoptc = LopTCController.getLopWhere(loptc.getMaMH(), loptc.getNienKhoa(), loptc.getNhom(), loptc.getHocKy());
            if (Integer.valueOf(jTextField_svmin.getText()) > Integer.valueOf(jTextField_svmax.getText())
                    || Integer.valueOf(jTextField_svmin.getText()) == Integer.valueOf(jTextField_svmax.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Sinh viên min không được lớn hơn hoặc bằng Sinh viên max!!!");
            } else if (ktLoptc != null) {
                JOptionPane.showMessageDialog(rootPane, "Không được trùng Mã Môn Học, Niên Khoá, Nhóm, Học Kỳ!!!");
            } else {
                int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn Thêm không?");
                if (confirm == JOptionPane.YES_OPTION) {
                    try {
                        LopTCController.themLopTC(loptc);
                        JOptionPane.showMessageDialog(rootPane, "Thêm thành công!!!");
                        reSet();
                        list.add(loptc);
                        showResult();
                        taoma();
                        ktHK = false;
                        ktNhom = false;
                        ktsvmax = false;
                        ktsvmin = false;
                    } catch (Exception e) {
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống!!!");
        }


    }//GEN-LAST:event_jButton_themActionPerformed

