    private void laySinhVien(String maSV) {
        jLabel_masv.setText(maSV);
        Sinhvien sv = SinhVienController.ktTrung_MASV(maSV);
        jTextField_diachi.setText(sv.getDiaChi());
        jTextField_sdt.setText(sv.getSDT());
        jTextField_ngaysinh.setText(sv.getNgaySinh());
        jTextField_hoten.setText(sv.getHoTen());
        jTextField_namnh.setText(String.valueOf(sv.getNamNH()));
        jComboBox_malop.setSelectedItem(sv.getMalOP());
        if (sv.getGoiTinh().equals("Nam")) {
            jRadioButton_nam.doClick();
        } else {
            jRadioButton_nu.doClick();
        }
    }

