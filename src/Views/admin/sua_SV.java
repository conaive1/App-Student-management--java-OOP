    private void jButton_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_suaActionPerformed
        String Gioitinh = null;
        String HoTen = jTextField_hoten.getText().trim();
        String NgaySinh = jTextField_ngaysinh.getText().trim();
        String SDT = jTextField_sdt.getText().trim();
        String DiaChi = jTextField_diachi.getText().trim();
        String NAMNH = jTextField_namnh.getText().trim();
        if (ktDiachi == true && ktHoten == true  && ktNamNh == true && ktSDT == true && ktdate == true) {
            if (!jRadioButton_nam.isSelected() && !jRadioButton_nu.isSelected()) {
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn giới tính!!!");
            } else {
                HoTen = xuly_All.chuanHoaDanhTuRieng(HoTen);
                DiaChi = xuly_All.chuanHoaDanhTuRieng(DiaChi);
                if (jRadioButton_nam.isSelected()) {
                    Gioitinh = jRadioButton_nam.getText();
                } else {
                    Gioitinh = jRadioButton_nu.getText();
                }
                int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn Sửa không?");
                if (confirm == JOptionPane.YES_OPTION) {
                    try {
                        SinhVienController.suaSinhVien(jLabel_masv.getText(), HoTen, NgaySinh, DiaChi, Integer.valueOf(NAMNH), (String) jComboBox_malop.getSelectedItem(), SDT, Gioitinh);
                        JOptionPane.showMessageDialog(this, "Sửa thành công!!!");
                        this.setVisible(false);
                        this.a.setVisible(false);
                        new QuanLy_SV().setVisible(true);
                    } catch (Exception ex) {
                        Logger.getLogger(QuanLy_SV.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống!!!");
        }
    }//GEN-LAST:event_jButton_suaActionPerformed

    private void jTextField_ngaysinhKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_ngaysinhKeyReleased

        if (jTextField_ngaysinh.getText().equals("")) {
            jLabel_loingaysinh.setText("Ngày sinh không được bỏ trống!!!");
            jLabel_loingaysinh.setVisible(true);
            ktdate = false;
        } else if (!jTextField_ngaysinh.getText().matches("\\d{1,2}[-|/]\\d{1,2}[-|/]\\d{4}")) {
            jLabel_loingaysinh.setText("Ngày sinh sai định dạng!!!");
            jLabel_loingaysinh.setVisible(true);
            ktdate = false;
        } else {
            jLabel_loingaysinh.setVisible(false);
            ktdate = true;
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTextField_ngaysinhKeyReleased

